from scrapy.item import Item, Field

class IPItem(Item):
    status = Field()
    date = Field()
    lat = Field()
    lon = Field()
    timezone = Field()
    org = Field()

class ProxyItem(Item):
    status = Field()
    date = Field()
    ip = Field()

