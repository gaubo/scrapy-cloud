	created_date = Field()
	last_crawled_date = Field()
	last_indexed_date = Field()
	property_id = Field()
	link = Field()
	listing_type = Field()
	property_size_raw = Field()
	property_size = Field()
	property_size_unit = Field()
	property_price_raw = Field()
	property_price = Field()
	property_price_unit = Field()
	property_area = Field()
	posted_date = Field()

